import {
  Body,
  Controller,
  Delete,
  Get,
  Param,
  Patch,
  Post,
  UseGuards,
} from '@nestjs/common';
import { JwtAuthGuard } from 'src/auth/jwt-auth.guard';
import { createContentManagementDto } from './dto/content-management.dto';
import { ContentManagementService } from './content-management.service';

@Controller('content-management')
@UseGuards(JwtAuthGuard)
export class ContentManagementController {
  constructor(private readonly service: ContentManagementService) {}

  @Post()
  create(@Body() dto: createContentManagementDto) {
    return this.service.create(dto);
  }

  @Get()
  findAll() {
    return this.service.findAll();
  }

  @Get(':id')
  findOne(@Param('id') id: number) {
    return this.service.findOne(id);
  }

  @Patch(':id')
  update(@Param('id') id: number, @Body() dto: createContentManagementDto) {
    return this.service.update(id, dto);
  }

  @Delete(':id')
  remove(@Param('id') id: number) {
    return this.service.remove(id);
  }
}
